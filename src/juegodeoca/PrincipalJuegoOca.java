    private void menuConfigGloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuConfigGloActionPerformed
        String clave = JOptionPane.showInputDialog(escritorio, "Ingrese contrase√±a", "Login", HEIGHT);

        if (clave.equalsIgnoreCase("panda")) {
            FrmConfiguracion frmconfig = new FrmConfiguracion();
            escritorio.add(frmconfig);
            frmconfig.toFront();
            frmconfig.setLocation(0, 0);
            frmconfig.setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(escritorio, "USTED NO ES UN PANDA","Error Fatal", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_menuConfigGloActionPerformed

